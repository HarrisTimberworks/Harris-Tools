#!/usr/bin/env node
/**
 * C3 — buildCapacityViewDoc pure function.
 *
 * Tests for the full-doc generator that concatenates 8 weekly sections
 * (C2 buildWeekSection) with a header (generation timestamp + source +
 * edit pointer) and a static symbol legend.
 *
 * Locked shape per chat-approved D8–D11:
 *   D8. Rolling-window start = getMondayOfWeek(options.generatedAt || new Date())
 *       Single source of truth — generatedAt drives both header timestamp
 *       and the 8-week window start.
 *   D9. Empty weeks render full structure (heading "0.00 crew hrs" + empty
 *       table + priority order label only). C2's behavior already covers
 *       this — no new C3 code path.
 *   D10. Header format:
 *        **Generated:** YYYY-MM-DD HH:MM (local) • **Source:** auto-generated
 *        by `scripts/run-planner.js --plan` • **Edit:** Manual Overrides
 *        board (18413101550)
 *        Trailing --- divider separates header from first week section.
 *   D11. Legend heading: ## Legend (NOT "& notes" — that suffix was
 *        operator hand-added context, which C3 doesn't generate).
 *        Static symbol meanings only:
 *          - Load convention (🔴/🟡/blank)
 *          - Pinned *(pinned)* italic
 *          - Subs *(sub)* italic + N / — Load shape
 *          - Job dates 📌/🎯/🚚
 *          - 🔧 override-driven cell
 *          - Refresh instruction
 *
 * Function signature:
 *   buildCapacityViewDoc(plan, jobsById, timeOff, options?) → string
 *   options:
 *     - generatedAt?: Date (default new Date())
 *     - acceptedOverrides?: Array<{jobId, station, crew, week}> (default [])
 *
 * Synthetic fixtures only — no monday I/O.
 */

const { buildCapacityViewDoc } = require('./capacity-view-generator.js');

const failures = [];
let checks = 0;
function check(label, cond, detail = '') {
  checks++;
  if (cond) {
    console.log(`  ✓ ${label}`);
  } else {
    failures.push(`${label}: ${detail}`);
    console.log(`  ✗ ${label} — ${detail}`);
  }
}

// ---------------------------------------------------------------------------
// Fixture builders
// ---------------------------------------------------------------------------

function makePlan(overrides = {}) {
  return {
    placements: [],
    capacityGrid: {},
    finishingCycleReport: { rows: [] },
    ...overrides,
  };
}

const JOBS_BY_ID = {
  'PL-A': { name: 'MAG - Roster 5 — Frameless (P1)', delivery: '2026-06-12', status: 'Scheduled' },
  'PL-B': { name: 'F&B - Quince Ave',                 delivery: '2026-06-19', status: 'Scheduled' },
  'PL-C': { name: 'SH - McMorris',                    delivery: '2026-07-03', status: 'Scheduled' },
};

// A reference date for deterministic tests: 2026-06-03 (Wednesday), so
// Monday-of-week is 2026-06-01. The 8-week window from there covers
// 2026-06-01 through 2026-07-20 (the last Monday).
const REF_DATE = new Date('2026-06-03T14:32:00');  // Wed, local time
const EXPECTED_WEEKS = [
  '2026-06-01', '2026-06-08', '2026-06-15', '2026-06-22',
  '2026-06-29', '2026-07-06', '2026-07-13', '2026-07-20',
];

// ---------------------------------------------------------------------------

(async () => {

  console.log('Test 1: header — Generated timestamp from options.generatedAt is in the output (deterministic format)');
  {
    const out = buildCapacityViewDoc(makePlan(), JOBS_BY_ID, [], { generatedAt: REF_DATE });
    // Format: YYYY-MM-DD HH:MM (local time). REF_DATE is constructed with a
    // local-time wall clock at 14:32, so the output should contain
    // "2026-06-03 14:32".
    check('header contains "**Generated:** 2026-06-03 14:32"',
      /\*\*Generated:\*\* 2026-06-03 14:32/.test(out),
      out.split('\n').find(l => l.includes('Generated:')) || 'no Generated line');
  }

  console.log('\nTest 2: header — three bullet pieces separated by " • " in the order Generated / Source / Edit');
  {
    const out = buildCapacityViewDoc(makePlan(), JOBS_BY_ID, [], { generatedAt: REF_DATE });
    const headerLine = out.split('\n').find(l => l.includes('**Generated:**')) || '';
    check('contains **Source:**',
      /\*\*Source:\*\* auto-generated by `scripts\/run-planner\.js --plan`/.test(headerLine),
      headerLine);
    check('contains **Edit:** Manual Overrides board (18413101550)',
      /\*\*Edit:\*\* Manual Overrides board \(18413101550\)/.test(headerLine),
      headerLine);
    // Order: Generated bullet, then Source bullet, then Edit bullet
    const genIdx = headerLine.indexOf('**Generated:**');
    const srcIdx = headerLine.indexOf('**Source:**');
    const editIdx = headerLine.indexOf('**Edit:**');
    check('Generated before Source before Edit',
      genIdx >= 0 && genIdx < srcIdx && srcIdx < editIdx,
      `genIdx=${genIdx}, srcIdx=${srcIdx}, editIdx=${editIdx}`);
    check('bullet separators " • " between pieces', /• \*\*Source:/.test(headerLine) && /• \*\*Edit:/.test(headerLine), headerLine);
  }

  console.log('\nTest 3: header — trailing "---" divider separates header from the first week section');
  {
    const out = buildCapacityViewDoc(makePlan(), JOBS_BY_ID, [], { generatedAt: REF_DATE });
    // Header line, blank, ---, blank, then "## Week of …"
    const lines = out.split('\n');
    const headerIdx = lines.findIndex(l => l.includes('**Generated:**'));
    const firstWeekIdx = lines.findIndex(l => /^## Week of /.test(l));
    check('header line found', headerIdx >= 0, `headerIdx=${headerIdx}`);
    check('first week heading found', firstWeekIdx >= 0, `firstWeekIdx=${firstWeekIdx}`);
    const between = lines.slice(headerIdx + 1, firstWeekIdx).join('\n');
    check('--- divider sits between header and first week section',
      /---/.test(between),
      `between=${JSON.stringify(between)}`);
  }

  console.log('\nTest 4: 8-week iteration — produces 8 "## Week of …" headings in date order');
  {
    const out = buildCapacityViewDoc(makePlan(), JOBS_BY_ID, [], { generatedAt: REF_DATE });
    const headings = out.split('\n').filter(l => /^## Week of /.test(l));
    check('exactly 8 week headings', headings.length === 8, `count=${headings.length}: ${JSON.stringify(headings)}`);
    // Heading format: "## Week of M/D — 0.00 crew hrs" (empty plan).
    // Expected Mondays (M/D no-zero-pad): 6/1, 6/8, 6/15, 6/22, 6/29, 7/6, 7/13, 7/20.
    const expectedMD = ['6/1', '6/8', '6/15', '6/22', '6/29', '7/6', '7/13', '7/20'];
    for (let i = 0; i < expectedMD.length; i++) {
      check(`heading ${i + 1} is "Week of ${expectedMD[i]}"`,
        headings[i] && headings[i].startsWith(`## Week of ${expectedMD[i]} `),
        headings[i] || '(missing)');
    }
  }

  console.log('\nTest 5: empty plan — all 8 weeks render "0.00 crew hrs" (D9 — empty weeks rendered, not skipped)');
  {
    const out = buildCapacityViewDoc(makePlan(), JOBS_BY_ID, [], { generatedAt: REF_DATE });
    const zeroHeadings = out.match(/## Week of \d+\/\d+ — 0\.00 crew hrs/g) || [];
    check('8 zero-hour headings present', zeroHeadings.length === 8, `count=${zeroHeadings.length}`);
  }

  console.log('\nTest 6: legend — present with ## Legend heading (not "& notes")');
  {
    const out = buildCapacityViewDoc(makePlan(), JOBS_BY_ID, [], { generatedAt: REF_DATE });
    check('## Legend heading present', /^## Legend\b/m.test(out), 'no "## Legend" heading found');
    check('NOT "## Legend & notes"', !/## Legend & notes/.test(out), 'legacy heading appeared');
  }

  console.log('\nTest 7: legend — all symbol categories + 🔧 line present');
  {
    const out = buildCapacityViewDoc(makePlan(), JOBS_BY_ID, [], { generatedAt: REF_DATE });
    const legendStart = out.indexOf('## Legend');
    const legendBody = legendStart >= 0 ? out.slice(legendStart) : '';
    check('Load convention with 🔴/🟡/blank',
      /\*\*Load:\*\*/.test(legendBody) && /🔴/.test(legendBody) && /🟡/.test(legendBody),
      legendBody.slice(0, 400));
    check('Pinned line with *(pinned)*',
      /\*\*Pinned\*\* \*\(pinned\)\*/.test(legendBody),
      legendBody);
    check('Subs line with *(sub)* + N / —',
      /\*\*Subs\*\* \*\(sub\)\*/.test(legendBody) && /N \/ —/.test(legendBody),
      legendBody);
    check('Job dates emoji 📌 🎯 🚚',
      /📌/.test(legendBody) && /🎯/.test(legendBody) && /🚚/.test(legendBody),
      legendBody);
    check('🔧 override-driven cell line',
      /\*\*🔧:\*\*/.test(legendBody) && /Manual Overrides board/.test(legendBody),
      legendBody);
    check('Refresh instruction with run-planner.js --plan',
      /\*\*Refresh:\*\*/.test(legendBody) && /run-planner\.js --plan/.test(legendBody),
      legendBody);
  }

  console.log('\nTest 8: legend — separator --- between last week section and legend heading');
  {
    const out = buildCapacityViewDoc(makePlan(), JOBS_BY_ID, [], { generatedAt: REF_DATE });
    const legendIdx = out.indexOf('## Legend');
    // Look at the 200 chars before "## Legend" — should include a "---" divider
    const tail = out.slice(Math.max(0, legendIdx - 200), legendIdx);
    check('--- divider before ## Legend', /---/.test(tail), `tail=${JSON.stringify(tail.slice(-100))}`);
  }

  console.log('\nTest 9: rolling-window start — defaults to today\'s Monday-of when no generatedAt provided');
  {
    // Without generatedAt, the first heading should be the Monday-of-today.
    // We can't assert a specific date (depends on when tests run), but we
    // can confirm: (a) some week heading exists, (b) it matches the
    // current week's Monday format, (c) it differs from REF_DATE's window
    // unless run on the right day.
    const out = buildCapacityViewDoc(makePlan(), JOBS_BY_ID, []);
    const firstHeading = out.split('\n').find(l => /^## Week of /.test(l));
    check('first heading present in default-now mode', !!firstHeading, 'no week heading');
    check('heading matches "## Week of M/D — ..." shape',
      /^## Week of \d+\/\d+ — \d+\.\d{2} crew hrs$/.test(firstHeading || ''),
      firstHeading);
  }

  console.log('\nTest 10: acceptedOverrides propagation — 🔧 lands in any week with a matching tuple');
  {
    const plan = makePlan({
      placements: [
        // Three placements, one in each of three different weeks within the
        // rolling window (6-01, 6-15, 6-29). All three are matched by
        // acceptedOverrides → all three Hrs cells should carry 🔧.
        { crew: 'Ian',     week: '2026-06-01', jobId: 'PL-A', station: 'Benchwork', hours: 8 },
        { crew: 'Spencer', week: '2026-06-15', jobId: 'PL-A', station: 'Benchwork', hours: 8 },
        { crew: 'Bob',     week: '2026-06-29', jobId: 'PL-A', station: 'Benchwork', hours: 8 },
      ],
      capacityGrid: {
        Ian:     { '2026-06-01': { committed: 8, avail: 40 } },
        Spencer: { '2026-06-15': { committed: 8, avail: 40 } },
        Bob:     { '2026-06-29': { committed: 8, avail: 40 } },
      },
    });
    const acceptedOverrides = [
      { jobId: 'PL-A', station: 'Benchwork', crew: 'Ian',     week: '2026-06-01' },
      { jobId: 'PL-A', station: 'Benchwork', crew: 'Spencer', week: '2026-06-15' },
      { jobId: 'PL-A', station: 'Benchwork', crew: 'Bob',     week: '2026-06-29' },
    ];
    const out = buildCapacityViewDoc(plan, JOBS_BY_ID, [], { generatedAt: REF_DATE, acceptedOverrides });
    const wrenchCount = (out.match(/🔧/g) || []).length;
    // Each match in the table is ONE wrench (8 placements × 1 = 3 total wrenches in tables).
    // Plus 1 wrench in the legend symbol line.
    check('🔧 appears 3 times in tables + 1 in legend (4 total)',
      wrenchCount === 4,
      `wrenchCount=${wrenchCount}`);
  }

  console.log('\nTest 11: out-of-window placements — ignored (not rendered anywhere)');
  {
    const plan = makePlan({
      placements: [
        // In window (6-08): should render
        { crew: 'Ian', week: '2026-06-08', jobId: 'PL-A', station: 'Benchwork', hours: 8 },
        // Out of window (way past 7-20): should NOT render
        { crew: 'Ian', week: '2026-09-07', jobId: 'PL-A', station: 'Benchwork', hours: 99 },
        // Out of window (before 6-01): should NOT render
        { crew: 'Ian', week: '2026-05-25', jobId: 'PL-A', station: 'Benchwork', hours: 77 },
      ],
      capacityGrid: {
        Ian: {
          '2026-06-08': { committed: 8,  avail: 40 },
          '2026-09-07': { committed: 99, avail: 40 },
          '2026-05-25': { committed: 77, avail: 40 },
        },
      },
    });
    const out = buildCapacityViewDoc(plan, JOBS_BY_ID, [], { generatedAt: REF_DATE });
    check('the 8h placement renders (in window)', /\| Ian \| 8 \/ 40 \|.+Bench \| 8 \|/.test(out), 'expected Ian/6-08/Bench/8 row');
    check('the 99h placement does NOT render (after window)', !/\b99\b/.test(out), 'unexpected 99 in output');
    check('the 77h placement does NOT render (before window)', !/\b77\b/.test(out), 'unexpected 77 in output');
  }

  console.log('\nTest 12: end-to-end mixed-shape — header + 8 sections + legend produces a structurally valid doc');
  {
    const plan = makePlan({
      placements: [
        // Wk 6-01: Ian + Spencer + Bob + sub
        { crew: 'Ian',     week: '2026-06-01', jobId: 'PL-A', station: 'Benchwork',            hours: 8, pinned: true },
        { crew: 'Ian',     week: '2026-06-01', jobId: 'PL-A', station: 'Pre Fin Cab Assembly', hours: 4 },
        { crew: 'Spencer', week: '2026-06-01', jobId: 'PL-A', station: 'Post Fin Cab Assembly', hours: 6 },
        { crew: 'Bob',     week: '2026-06-01', jobId: 'PL-B', station: 'Benchwork',             hours: 19.95 },
        // Wk 6-08: Ian benchwork
        { crew: 'Ian',     week: '2026-06-08', jobId: 'PL-A', station: 'Benchwork',             hours: 12 },
      ],
      capacityGrid: {
        Ian:     {
          '2026-06-01': { committed: 12,    avail: 40 },
          '2026-06-08': { committed: 12,    avail: 40 },
        },
        Spencer: { '2026-06-01': { committed: 6,     avail: 40 } },
        Bob:     { '2026-06-01': { committed: 19.95, avail: 40 } },
      },
      finishingCycleReport: {
        rows: [{ jobId: 'PL-A', jobName: 'MAG R5-P1', finishDrop: '2026-06-05', finishReturn: '2026-06-19', valid: true }],
      },
    });
    const timeOff = [{ crew: 'Rob', week: '2026-06-01', hours: 40 }];
    const acceptedOverrides = [{ jobId: 'PL-B', station: 'Benchwork', crew: 'Bob', week: '2026-06-01' }];
    const out = buildCapacityViewDoc(plan, JOBS_BY_ID, timeOff, { generatedAt: REF_DATE, acceptedOverrides });

    // Header at the very top
    check('output starts with header line',
      /^\*\*Generated:\*\* /.test(out),
      out.slice(0, 80));
    // 8 week headings
    const weekCount = (out.match(/^## Week of /gm) || []).length;
    check('8 week headings', weekCount === 8, `weekCount=${weekCount}`);
    // Legend at the bottom
    check('## Legend heading after all week sections',
      /## Legend[\s\S]*$/.test(out) && out.lastIndexOf('## Week of ') < out.indexOf('## Legend'),
      'Legend should be after the last week section');
    // First week's content surfaces
    check('week 6/1 heading has correct hour total (37.95)',
      /## Week of 6\/1 — 37\.95 crew hrs/.test(out),
      out.match(/## Week of 6\/1[^\n]*/)?.[0] || 'no 6/1 heading');
    // Wk 6/1 finish drop appears
    check('📌 Fri 6/5 finish drop line in wk 6/1 section',
      /📌 Fri 6\/5 — MAG R5-P1 finish drop/.test(out),
      'expected finish-drop line');
    // Wk 6/1 sub row
    check('Bob row in wk 6/1 has 🔧 prefix (acceptedOverrides matched)',
      /\| Bob \| 19\.95 \/ 40 \| F&B - Quince Ave \| Bench \| 🔧 19\.95 \|/.test(out),
      'expected Bob 🔧 row');
    // Wk 6/8 surfaces
    check('week 6/8 heading total 12.00',
      /## Week of 6\/8 — 12\.00 crew hrs/.test(out),
      'expected 6/8 heading');
    // PTO row only on wk 6/1 (timeOff entry only for that week)
    const ptoMatches = out.match(/\| Rob \| PTO/g) || [];
    check('Rob PTO row appears exactly once (only wk 6/1)', ptoMatches.length === 1, `ptoMatches=${ptoMatches.length}`);
  }

  console.log('\nTest 13: default acceptedOverrides — no 🔧 in any table cell (backwards-compat when omitted)');
  {
    const plan = makePlan({
      placements: [
        { crew: 'Ian', week: '2026-06-01', jobId: 'PL-A', station: 'Benchwork', hours: 8 },
      ],
      capacityGrid: { Ian: { '2026-06-01': { committed: 8, avail: 40 } } },
    });
    const out = buildCapacityViewDoc(plan, JOBS_BY_ID, [], { generatedAt: REF_DATE });
    // The legend always has a 🔧 (it's a static symbol explanation). The
    // table should have ZERO. So total 🔧 count is exactly 1.
    const wrenchCount = (out.match(/🔧/g) || []).length;
    check('🔧 appears only once (in the legend, not in any table cell)',
      wrenchCount === 1,
      `wrenchCount=${wrenchCount}`);
  }

  // Task 10 (2026-06-12) — current-week header annotation + clamp block

  console.log('\nTest 14: current-week header annotated when nowContext present and mid-week');
  {
    // plan.nowContext = { currentWeekMonday:'2026-06-01', effectiveWeek:'2026-06-01', remainingWorkdays:2, isMidWeek:true }
    // generatedAt is REF_DATE → window starts 2026-06-01 which IS currentWeekMonday
    const plan = makePlan({
      nowContext: {
        currentWeekMonday: '2026-06-01',
        effectiveWeek: '2026-06-01',
        remainingWorkdays: 2,
        isMidWeek: true,
      },
    });
    const out = buildCapacityViewDoc(plan, JOBS_BY_ID, [], { generatedAt: REF_DATE });
    const headings = out.split('\n').filter(l => /^## Week of /.test(l));
    const firstHeading = headings[0] || '';
    check('week 6/1 heading contains in-progress annotation',
      /in progress — 2 of 5 workdays remain/.test(firstHeading),
      `firstHeading=${firstHeading}`);
    check('week 6/8 heading does NOT have annotation',
      !/in progress/.test(headings[1] || ''),
      `secondHeading=${headings[1]}`);
  }

  console.log('\nTest 15: no nowContext (legacy plan JSON) → headers unchanged');
  {
    const plan = makePlan();  // no nowContext key
    const out = buildCapacityViewDoc(plan, JOBS_BY_ID, [], { generatedAt: REF_DATE });
    const hasAnnotation = /in progress/.test(out);
    check('no in-progress annotation without nowContext', !hasAnnotation, hasAnnotation ? 'annotation present' : 'clean');
    // Confirm headings still render normally
    const headings = out.split('\n').filter(l => /^## Week of /.test(l));
    check('still 8 headings', headings.length === 8, `count=${headings.length}`);
    check('week 6/1 heading is plain (0.00)', /^## Week of 6\/1 — 0\.00 crew hrs$/.test(headings[0] || ''), headings[0]);
  }

  console.log('\nTest 16: clamp block renders when windowClamps is non-empty');
  {
    const plan = makePlan({
      windowClamps: [
        {
          jobId: 'PL-A',
          jobName: 'MAG - BCH',
          station: 'bench',
          computedStart: '2026-06-08',
          computedEnd: '2026-06-12',
          clampedStart: '2026-06-15',
          clampedEnd: '2026-06-19',
          entirelyPast: false,
        },
        {
          jobId: 'PL-B',
          jobName: 'F&B - Quince',
          station: 'panel',
          computedStart: '2026-06-01',
          computedEnd: '2026-06-05',
          clampedStart: '2026-06-08',
          clampedEnd: '2026-06-12',
          entirelyPast: true,
        },
      ],
    });
    const out = buildCapacityViewDoc(plan, JOBS_BY_ID, [], { generatedAt: REF_DATE });
    check('clamp section heading present',
      /## ⏰ Late work pulled forward/.test(out),
      'heading not found');
    check('BCH bench clamp line present',
      /\*\*MAG - BCH\*\* \/ bench: computed 2026-06-08–2026-06-12 → 2026-06-15–2026-06-19/.test(out),
      out.slice(out.indexOf('⏰') >= 0 ? out.indexOf('⏰') : 0, out.indexOf('⏰') + 300));
    check('BCH bench line does NOT have entirely-past marker (entirelyPast:false)',
      !(/\*\*MAG - BCH\*\*.*entirely past/.test(out)),
      'unexpected entirely-past marker on BCH');
    check('F&B panel line has entirely-past marker (entirelyPast:true)',
      /\*\*F&B - Quince\*\*.*entirely past — late work/.test(out),
      out.slice(out.indexOf('⏰') >= 0 ? out.indexOf('⏰') : 0, out.indexOf('⏰') + 400));
  }

  console.log('\nTest 17: no clamps → clamp block absent entirely');
  {
    const plan = makePlan({ windowClamps: [] });
    const out = buildCapacityViewDoc(plan, JOBS_BY_ID, [], { generatedAt: REF_DATE });
    check('no clamp section when windowClamps is empty array',
      !/⏰ Late work pulled forward/.test(out),
      'clamp section appeared unexpectedly');
    const planNoKey = makePlan();  // windowClamps key absent
    const out2 = buildCapacityViewDoc(planNoKey, JOBS_BY_ID, [], { generatedAt: REF_DATE });
    check('no clamp section when windowClamps key absent',
      !/⏰ Late work pulled forward/.test(out2),
      'clamp section appeared without windowClamps key');
  }

  console.log();
  if (failures.length > 0) {
    console.log(`❌ ${failures.length} failure(s) of ${checks} checks:`);
    for (const f of failures) console.log(`  - ${f}`);
    process.exit(1);
  }
  console.log(`✅ All C3 capacity-view-doc tests passed (${checks} checks).`);

})().catch(err => {
  console.error('Fatal:', err);
  process.exit(1);
});
