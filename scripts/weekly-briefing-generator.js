// C6 — Weekly Briefing generator.
//
// Pure functions, no monday I/O. Single-week printable for the Monday
// meeting (design spec "Weekly Briefing doc"; plan doc §C C6 + §B D2).
// The week body IS a C2 buildWeekSection — same key-dates block, crew
// table, and priority order as one Capacity View week. Briefing-specific
// framing is the H1 title + Generated header. No legend (spec's briefing
// shape is top notes + crew table + priority order only).
//
// Consumed by C7 (write-weekly-briefing.js) and C8 (run-planner.js wire-up).

const {
  buildWeekSection,
  formatGeneratedAt,
  toMondayISO,
} = require('./capacity-view-generator.js');

// Which week does a run brief? UTC-day semantics, consistent with the rest
// of the codebase's date math:
//   - Sat/Sun → NEXT Monday. The Saturday-6pm scheduled run briefs the
//     upcoming week. (Friday-evening Mountain runs land on Saturday UTC,
//     which matches the Friday-shutdown plan-next-week use case.)
//   - Mon–Fri → Monday of the CURRENT week. A mid-week on-demand run briefs
//     the week in progress.
function briefingWeekFor(date) {
  const d = new Date(date);
  const day = d.getUTCDay();
  if (day === 6) d.setUTCDate(d.getUTCDate() + 2);       // Sat → Mon
  else if (day === 0) d.setUTCDate(d.getUTCDate() + 1);  // Sun → Mon
  return toMondayISO(d);
}

// D2: doc name carries the full ISO Monday date, e.g.
// "📋 HTW Weekly Briefing — Week of 2026-06-01".
function buildWeeklyBriefingTitle(weekISO) {
  return `📋 HTW Weekly Briefing — Week of ${weekISO}`;
}

// → { title, weekISO, markdown }. options:
//   - generatedAt: Date (default now) — header timestamp + default week
//   - weekISO: explicit week override (tests / operator control)
//   - acceptedOverrides: C5 tuple set for 🔧 cells
function buildWeeklyBriefingDoc(plan, jobsById, timeOff, options) {
  const generatedAt = (options && options.generatedAt) || new Date();
  const weekISO = (options && options.weekISO) || briefingWeekFor(generatedAt);
  const acceptedOverrides = (options && options.acceptedOverrides) || [];
  const title = buildWeeklyBriefingTitle(weekISO);

  const header = [
    `# ${title}`,
    '',
    `**Generated:** ${formatGeneratedAt(generatedAt)} • **Source:** auto-generated by \`scripts/run-planner.js --plan\` • **Edit:** Manual Overrides board (18413101550)`,
    '',
    '---',
    '',
  ].join('\n');

  const body = buildWeekSection(weekISO, plan, jobsById, timeOff, { acceptedOverrides });
  return { title, weekISO, markdown: header + body };
}

module.exports = {
  briefingWeekFor,
  buildWeeklyBriefingTitle,
  buildWeeklyBriefingDoc,
};
